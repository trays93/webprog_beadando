<h2> Bemutatkozás</h2>
				
<p>Tengerimalac-rajongóként 2011-ben fogalmazódott meg bennünk a gondolat, hogy létrehozzunk egy egyesületet, hogy szervezetként még többet tehessünk a rászoruló tengerimalacokért.</p>
<p>2012. októbertől végül hivatalosan is megalakult hazánk egyetlen, speciálisan tengerimalac-mentésre szakosodott „érdekvédelmi” szervezete.</p>
