<p>Legfontosabb feladatunk, hogy a rossz, illetve helytelen körülmények között tartott, kidobott (erdőbe, kukába, utcára, szeméttelepre) vagy leadott tengerimalacokat megmentsük, rehabilitáljuk, szocializáljuk, majd új otthont és felelősségteljes gazdikat találjunk nekik.</p>
<p>Küldetésünk, hogy minél több emberrel megismertessük a felelős állattartást, és szakmailag korrekt információkhoz juttassuk a tengerimalacok tartásában kevésbé járatos gazdikat, legyen szó akár az élőhely kialakításáról, a táplálásról, az orvosi ellátásról vagy a pártalálásról.</p>
<p>Munkánk során rendkívüli segítséget jelent az egzotikus kisállatokkal foglalkozó szakorvosok és asszisztenseik által nyújtott szaktudás és szakmai háttér, kiemelve a Állatorvosi Egyetem Egzotikusállat- és Vadegészségügyi Tanszék Klinikáján dolgozó Csatári Dóra doktornőt és az Exo-Pet Állatgyógyászati Centrum mögött álló Pazár Péter és Sátorhelyi Tamás doktor urat. Önzetlen segítségüket ezúton is köszönjük!</p>
<p>Meggyőződésünk, hogy a széles körű összefogással minden egyszerűbb és sikeresebb, ezért keressük az együttműködés lehetőségét az állami, önkormányzati, hasonló profilú civil és a piaci szervezetekkel, szerveződésekkel, az írott és elektronikus média szereplőivel egyaránt.</p>
